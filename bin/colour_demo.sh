#!/bin/bash

# From https://misc.flogisoft.com/bash/tip_colors_and_formatting
for fgbg in 38 48 ; do
    for color in {0..255} ; do # Colors
        # Display the color
        printf "\e[${fgbg};5;%sm  %3s  \e[0m" $color $color
        # Display 6 colors per lines
        if [ $((($color + 1) % 6)) == 4 ] ; then
            echo # New line
        fi
    done
    echo # New line
done

